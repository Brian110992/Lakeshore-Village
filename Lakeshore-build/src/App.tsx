import { BrowserRouter as Router, Routes, Route } from 'react-router-dom';
import Navbar from './components/Navbar';
import Footer from './components/Footer';
import Home from './pages/Home';
import Search from './pages/Search';
import MarketReport from './pages/MarketReport';
import Gallery from './pages/Gallery';
import Contact from './pages/Contact';
import OnSiteAmenities from './pages/OnSiteAmenities';
import NearbyAmenities from './pages/NearbyAmenities';
import { useEffect } from 'react';
import { useLocation } from 'react-router-dom';

function ScrollToTop() {
  const { pathname } = useLocation();
  useEffect(() => {
    window.scrollTo(0, 0);
  }, [pathname]);
  return null;
}

export default function App() {
  return (
    <Router>
      <ScrollToTop />
      <div className="flex flex-col min-h-screen">
        <Navbar />
        <main className="flex-grow">
          <Routes>
            <Route path="/" element={<Home />} />
            <Route path="/search" element={<Search />} />
            <Route path="/amenities/on-site" element={<OnSiteAmenities />} />
            <Route path="/amenities/nearby" element={<NearbyAmenities />} />
            <Route path="/market-report" element={<MarketReport />} />
            <Route path="/gallery" element={<Gallery />} />
            <Route path="/contact" element={<Contact />} />
          </Routes>
        </main>
        <Footer />
      </div>
    </Router>
  );
}
